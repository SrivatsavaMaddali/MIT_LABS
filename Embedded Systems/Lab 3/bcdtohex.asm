	AREA RESET, DATA, READONLY
    EXPORT __Vectors
__Vectors  
    DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R11,=DST
	LDR R1,[R0]
	LDR R2,=0x01
	LDR R9,=0x08
	LDR R10,=0x0A
UP  AND R3,R1,#0x0F
	MLA R5,R3,R2,R5
	MUL R2,R10
	LSR R1,#4
	SUBS R9,#1
	BNE UP
	STR R5,[R11]
STOP
    B STOP
SRC DCD 0x255
	AREA data1,DATA,READWRITE
DST DCD 0
	END 