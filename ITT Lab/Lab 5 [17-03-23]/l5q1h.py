# define two sets
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# union of two sets
print("Union of sets: ", set1.union(set2))

# intersection of two sets
print("Intersection of sets: ", set1.intersection(set2))

# difference between two sets
print("Difference between sets: ", set1.difference(set2))

# symmetric difference between two sets
print("Symmetric difference between sets: ", set1.symmetric_difference(set2))
